import { Injectable } from "@angular/core";
import { Http } from "@angular/http";
import { Tag } from "./tag.model";
import { Observable } from "rxjs";
import { ExceptionService } from "../core";

@Injectable()
export class TagService {
    constructor(private _http: Http) { }

    public add(entity: Tag) {
        return this._http
            .post(`${this._baseUrl}/api/tag/add`, entity)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public get() {
        return this._http
            .get(`${this._baseUrl}/api/tag/get`)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public getById(options: { id: number }) {
        return this._http
            .get(`${this._baseUrl}/api/tag/getById?id=${options.id}`)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public remove(options: { id: number }) {
        return this._http
            .delete(`${this._baseUrl}/api/tag/remove?id=${options.id}`)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public get _baseUrl() { return ""; }

}
